int		ft_atoi(const char *str)
{
	int	i;
	int	ng;
	int	nb;

	i = 0;
	ng = 0;
	nb = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\f' || str[i] == '\v')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			ng = 1;
		if (str[i + 1] < '0' || str[i + 1] > '9')
			return (0);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (ng == 1 ? -nb : nb);
}
