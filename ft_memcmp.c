#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
    
	i = 0;
	if (!(s1) || !(s2))
		return (NULL);
	while (s1[i] && s2[i] && *s1 == *s2 && i < (n - 1))
		i++;
	return (s1[i] - s2[i]);
}
