int		ft_itoa_sizer(int nb)
{
	int	length;

	length = 0;
	if (nb < 0)
	{
		nb = -nb;
		length++;
	}
	while (nb)
	{
		nb = nb / 10;
		length++;
	}
	length++;
	return (length);
}
